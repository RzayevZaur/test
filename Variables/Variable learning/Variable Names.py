# # Dəyişən Adlar
# # Dəyişənin qısa adı (x və y kimi) və ya daha təsviredici adı ola bilər (yaş, carname, total_volume). Python dəyişənləri üçün qaydalar:
# # Dəyişən adı hərf və ya alt xətt simvolu ilə başlamalıdır
# # Dəyişən adı nömrə ilə başlaya bilməz
# # Dəyişən ad yalnız alfa-rəqəm simvollarından və alt xəttdən ibarət ola bilər (Az, 0-9 və _ )
# # Dəyişən adları hərflərə həssasdır (yaş, Yaş və YAŞ üç fərqli dəyişəndir)
# # Dəyişən adı Python açar sözlərindən heç biri ola bilməz .
#
#
# # myvar = "John"
# my_var = "John"
# # _my_var = "John"
# # myVar = "John"
# # MYVAR = "John"
# myvar2 = "John"
#
# #
# # # print(myvar)
# # print(my_var)
# # print(_my_var)
# # print(myVar)
# # print(MYVAR)
# # print(myvar2)
#
#
# Birdən çox sözdən ibarət dəyişən adlarını oxunaqlı etmək üçün bu texnikaları daha da aydın şəkildə izah edək:
#
# 1. Dəvə Qutusu (Camel Case)
# Bu üsulda dəyişən adının ilk sözü kiçik hərflərlə başlayır, sonrakı hər bir söz isə böyük hərflə başlayır.
# Bu üsul Python, JavaScript və Java kimi proqramlaşdırma dillərində geniş istifadə olunur.
#
# myVariableName = "John"
#
#
#
# 2. Paskal Qutusu (Pascal Case)
# Paskal üsulunda hər söz böyük hərflə başlayır. Bu üsul adətən C# və Visual Basic proqramlaşdırma dillərində istifadə olunur.
#
# Nümunə:
# MyVariableName = "John"
#
#
#
#
# 3. Snake Case
# Bu üsulda sözlər arasında boşluq əvəzinə alt xətt (_) işarəsi istifadə olunur. Bütün hərflər adətən kiçik hərflərlə yazılır. Python və bir çox sistem sənədləri bu üsulu dəstəkləyir.
#
# Nümunə:
# my_variable_name = "John"
#
#
# Hansını Seçmək Lazımdır?
# Dəvə Qutusu: Əgər JavaScript və ya Python kimi dillərdə işləyirsinizsə, bu üsul idealdır.
# Paskal Qutusu: Daha çox obyekt və ya sinif adları üçün istifadə olunur.
# Snake Case: Python-da sabit dəyişənlər (constants) və ya sistem faylları üçün standartdır.
