# Bir dəyişəni göstərmək: Məsələn, bir dəyişən var və biz onu ekrana çıxartmaq istəyirik:

# x = "Python çox gözəldir"
# print(x)


# Bu, ekrana "Python çox gözəldir" yazdıracaq.
# Bir neçə dəyişəni göstərmək: Əgər bir neçə dəyişəni bir yerdə göstərmək istəyirsənsə, sadəcə onları vergül ilə ayırmalısan:

# x = "Python"
# y = "çox"
# z = "gözəldir"
# print(x, y, z)
# Bu da ekrana "Python çox gözəldir" yazdıracaq.


# Dəyişənləri birləşdirmək: Əgər dəyişənləri birləşdirmək
# istəyirsənsə, + işarəsindən istifadə edə bilərsən:
#
# x = "Python "
# y = "çox "
# z = "gözəldir"
# print(x + y + z)

# Bu da ekrana "Python çox gözəldir" yazdıracaq. Amma unutma,
# burada boşluqları da əlavə etməliyik ki, sözlər arasında boşluq olsun.


# Rəqəmləri toplamaq: Əgər rəqəmləri toplamaq istəyirsənsə, + işarəsi bunu edir:
# x = 5
# y = 10
# print(x + y)
# Bu da ekrana 15 yazdıracaq.


#
# Dəyişənlərlə səhv: Amma, bir rəqəmi və yazını birləşdirməyə
# çalışsan, Python bunu etməyəcək və səhv verəcək:
#
# x = 5
